
//----------------------------------------------------------------------------------------------------------------------------
#include <iostream>
#include <fstream>
#include <cmath>
using namespace std;
    char file_interim[50]    = "interim.dat";
//----------------------------------------------------------------------------------------------------------------------------
const int column = 4; 
//----------------------------------------------------------------------------------------------------------------------------
int line_counter(char file[50])
{
std::ifstream infile(file);
char cc;
int line_count = 0;
while (infile.get(cc))
{
   if (cc == '\n')
        ++line_count;
}
return line_count;
}
//----------------------------------------------------------------------------------------------------------------------------
//
//                                                     - Load INPUT -  
//
//----------------------------------------------------------------------------------------------------------------------------
double load_interim(int i, int  variable , int lines )
{
std::ifstream infile(file_interim);
double x[column];
double numbers[lines];
int indx=1;
while(infile >> x[0] >> x[1] >> x[2] >> x[3]) 
{
numbers[indx-1]= x[variable];
indx = indx + 1;
}
double cdata = numbers[i];
return cdata;
}
//--------------------------------------------------------------------------------------------------------------------------------
// 
//                                                main
//
//--------------------------------------------------------------------------------------------------------------------------------
int main(){
int lines =  line_counter(file_interim);
double X[column][lines];
int i,j;
for(i=0;i<lines;i++){
for(j=0;j<column;j++){
X[j][i] = load_interim(i,j,lines);
}
}

//---------------------------------------------------------------------------------------------------------------------------------
// Find minimum Height 
//---------------------------------------------------------------------------------------------------------------------------------
double minh = abs(X[0][0]);
int indx; 
for(i=0;i<lines;i++){
if( abs( X[0][i] ) < minh && X[1][i] > 0){
minh = abs( X[0][i] ) ;
indx = i ; 
}
}
//----------------------------
double thmax = X[2][0];
for(i=0;i<=indx;i++){
if(X[2][i] > thmax){
thmax = X[2][i];
}
}
//---------------------------------------------------------------------------------------------------------------------------------
double Vtd = X[1][indx];
double Mfuel = X[3][0] - X[3][indx];
//----------------------------------------------------------------------------------------------------------------------------------
cout << minh << endl;
cout << Vtd << endl;
cout << thmax << endl;
cout << Mfuel << endl;
}
